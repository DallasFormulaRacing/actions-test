export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const [sensors, setSensors] = React.useState<{ sensor_id: string; name: string }[]>([]);

  React.useEffect(() => {
    async function fetchSensors() {
      try {
        const res = await fetch('http://localhost:5000/sensors');
        const data = await res.json();
        setSensors(data);
      } catch (error) {
        console.error('Error fetching sensors:', error);
      }
    }

    fetchSensors();
  }, []);

  const sensorList = (sensors || []).map((sensor) => ({
    id: sensor.sensor_id,
    name: sensor.name,
    url: `#/sensors/${sensor.sensor_id}`,
  }));

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <a href="#">
                <IconInnerShadowTop className="!size-5" />
                <span className="text-base font-semibold">Acme Inc.</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        {/* <NavMain items={data.navMain} /> */}
        <NavDocuments items={sensorList} />
        <NavSecondary items={data.navSecondary} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={data.user} />
      </SidebarFooter>
    </Sidebar>
  )
}
